const LEARNER_NAME_KEY = "learningSchool.learnerName";
const colors = ["#ff6b6b", "#ffd166", "#22c55e", "#0ea5e9", "#a855f7", "#f472b6", "#fb923c"];
const MATCH_BATCH_SIZE = 5;

function learnerName() {
  try {
    return (localStorage.getItem(LEARNER_NAME_KEY) || "").trim();
  } catch {
    return "";
  }
}

function learnerGreeting() {
  const name = learnerName();
  return name ? `Szia, ${name}!` : "";
}

const state = {
  screen: "lessons",
  mode: null,
  selectedLessonId: lessons[0].id,
  remaining: [],
  current: null,
  options: [],
  found: 0,
  feedback: "",
  feedbackType: "",
  locked: false,
  selectedLeft: null,
  matches: new Map(),
  matchOrder: [],
  matchBatchIndex: 0,
  batchWords: [],
  leftOrder: [],
  rightOrder: [],
  drag: null,
  runId: 0,
  timer: null,
};

function currentLesson() {
  return lessons.find((lesson) => lesson.id === state.selectedLessonId) || lessons[0];
}

function currentWords() {
  return currentLesson().words;
}

function currentSentences() {
  return currentLesson().sentences || [];
}

function hasSentences() {
  return currentSentences().length >= 4;
}

function currentItems() {
  return state.mode === "sentence" ? currentSentences() : currentWords();
}

function shuffle(items) {
  return [...items].sort(() => Math.random() - 0.5);
}

function sampleOptions(answer) {
  const pool = currentItems();
  const choices = shuffle(pool.filter((item) => item.id !== answer.id)).slice(0, 3);
  return shuffle([answer, ...choices]);
}

function selectLesson(id) {
  resetTimer();
  state.selectedLessonId = id;
  state.screen = "activities";
  state.mode = null;
  state.feedback = "";
  render();
}

function startRound(mode) {
  resetTimer();
  state.runId += 1;
  state.screen = "game";
  state.mode = mode;
  state.remaining = shuffle(mode === "sentence" ? currentSentences() : currentWords());
  state.found = 0;
  state.feedback = "";
  state.feedbackType = "";
  state.locked = false;
  nextQuestion();
}

function nextQuestion() {
  if (state.mode !== "word" && state.mode !== "picture" && state.mode !== "sentence") return;

  if (!state.remaining.length) {
    celebrate();
    return;
  }

  state.current = state.remaining[0];
  state.options = sampleOptions(state.current);
  state.feedback = "";
  state.feedbackType = "";
  state.locked = false;
  render();
}

function chooseAnswer(id) {
  if (state.locked) return;

  if (id !== state.current.id) {
    state.feedback = "Mai încearcă!";
    state.feedbackType = "try";
    flashAnswer(id, "wrong");
    renderFeedback();
    return;
  }

  state.locked = true;
  state.feedback = "Corect!";
  state.feedbackType = "good";
  state.found += 1;
  state.remaining.shift();
  flashAnswer(id, "correct");
  renderFeedback();
  renderProgress();

  const runId = state.runId;
  state.timer = setTimeout(() => {
    if (state.runId !== runId) return;
    nextQuestion();
  }, 900);
}

function flashAnswer(id, className) {
  const button = document.querySelector(`[data-answer="${id}"]`);
  if (!button) return;
  button.classList.remove("wrong", "correct");
  void button.offsetWidth;
  button.classList.add(className);
}

function renderFeedback() {
  const feedback = document.querySelector(".feedback");
  if (!feedback) return;
  feedback.textContent = state.feedback;
  feedback.className = `feedback show ${state.feedbackType}`;
}

function renderProgress() {
  const items = currentItems();
  const progress = document.querySelector(".progress-fill");
  const label = document.querySelector(".progress-label");
  if (!progress || !label) return;
  progress.style.width = `${(state.found / items.length) * 100}%`;
  label.textContent = `${state.found}/${items.length}`;
}

function startMatchMode() {
  resetTimer();
  state.runId += 1;
  state.screen = "game";
  state.mode = "match";
  state.found = 0;
  state.feedback = "";
  state.feedbackType = "";
  state.selectedLeft = null;
  state.matchOrder = shuffle(currentWords());
  state.matchBatchIndex = 0;
  state.drag = null;
  loadMatchBatch();
}

function loadMatchBatch() {
  const start = state.matchBatchIndex * MATCH_BATCH_SIZE;
  state.batchWords = state.matchOrder.slice(start, start + MATCH_BATCH_SIZE);
  state.matches = new Map();
  state.selectedLeft = null;
  state.leftOrder = shuffle(state.batchWords);
  state.rightOrder = shuffle(state.batchWords);
  state.drag = null;
  render();
  requestAnimationFrame(drawMatchLines);
}

function selectLeft(id) {
  if (state.matches.has(id)) return;
  state.selectedLeft = state.selectedLeft === id ? null : id;
  drawMatchLines();
  renderMatchItems();
}

function selectRight(id) {
  const leftId = state.selectedLeft;
  if (!leftId || [...state.matches.values()].includes(id)) return;
  checkPair(leftId, id);
}

function checkPair(leftId, rightId) {
  if (leftId !== rightId) {
    state.feedback = "Mai încearcă!";
    state.feedbackType = "try";
    state.selectedLeft = null;
    renderFeedback();
    markWrong(leftId, rightId);
    return;
  }

  state.matches.set(leftId, rightId);
  state.selectedLeft = null;
  state.found += 1;
  state.feedback = "Corect!";
  state.feedbackType = "good";
  renderMatchItems();
  drawMatchLines();
  renderFeedback();
  renderProgress();

  if (state.matches.size === state.batchWords.length) {
    const runId = state.runId;
    state.timer = setTimeout(() => {
      if (state.runId !== runId || state.mode !== "match") return;
      if (state.found >= currentWords().length) {
        celebrate();
        return;
      }
      state.matchBatchIndex += 1;
      state.feedback = "";
      state.feedbackType = "";
      loadMatchBatch();
    }, 650);
  }
}

function resetTimer() {
  if (!state.timer) return;
  clearTimeout(state.timer);
  state.timer = null;
}

function goLessons() {
  resetTimer();
  state.runId += 1;
  state.screen = "lessons";
  state.mode = null;
  state.drag = null;
  render();
}

function goActivities() {
  resetTimer();
  state.runId += 1;
  state.screen = "activities";
  state.mode = null;
  state.drag = null;
  state.feedback = "";
  render();
}

function markWrong(leftId, rightId) {
  const left = document.querySelector(`[data-left="${leftId}"]`);
  const right = document.querySelector(`[data-right="${rightId}"]`);
  [left, right].forEach((item) => {
    if (!item) return;
    item.classList.remove("wrong");
    void item.offsetWidth;
    item.classList.add("wrong");
    setTimeout(() => item.classList.remove("wrong"), 420);
  });
  renderMatchItems();
  drawMatchLines();
}

function handlePointerDown(event, id) {
  if (state.screen !== "game" || state.mode !== "match" || state.matches.has(id)) return;
  const board = document.querySelector(".match-board");
  const start = getConnectorPoint(`[data-left="${id}"]`, "right", board);
  state.drag = { leftId: id, x1: start.x, y1: start.y, x2: start.x, y2: start.y };
  state.selectedLeft = id;
  event.currentTarget.setPointerCapture?.(event.pointerId);
  renderMatchItems();
  drawMatchLines();
}

function handlePointerMove(event) {
  if (!state.drag) return;
  const board = document.querySelector(".match-board");
  const rect = board.getBoundingClientRect();
  state.drag.x2 = event.clientX - rect.left;
  state.drag.y2 = event.clientY - rect.top;
  drawMatchLines();
}

function handlePointerUp(event) {
  if (!state.drag) return;
  const rightElement = document.elementFromPoint(event.clientX, event.clientY)?.closest("[data-right]");
  const leftId = state.drag.leftId;
  state.drag = null;
  if (rightElement) {
    checkPair(leftId, rightElement.dataset.right);
  } else {
    state.selectedLeft = null;
    renderMatchItems();
    drawMatchLines();
  }
}

function renderMatchItems() {
  document.querySelectorAll(".match-item").forEach((item) => {
    const leftId = item.dataset.left;
    const rightId = item.dataset.right;
    const isLeftDone = leftId && state.matches.has(leftId);
    const isRightDone = rightId && [...state.matches.values()].includes(rightId);
    const isSelected = leftId && state.selectedLeft === leftId;
    item.classList.toggle("done", Boolean(isLeftDone || isRightDone));
    item.classList.toggle("selected", Boolean(isSelected));
    item.classList.toggle("dim", Boolean(state.selectedLeft && rightId && isRightDone));
  });
}

function getConnectorPoint(selector, side, board) {
  const element = document.querySelector(selector);
  const boardRect = board.getBoundingClientRect();
  const rect = element.getBoundingClientRect();
  return {
    x: side === "right" ? rect.right - boardRect.left : rect.left - boardRect.left,
    y: rect.top + rect.height / 2 - boardRect.top,
  };
}

function drawMatchLines() {
  const board = document.querySelector(".match-board");
  const svg = document.querySelector(".match-lines");
  if (!board || !svg) return;

  const lines = [];
  state.matches.forEach((rightId, leftId) => {
    const start = getConnectorPoint(`[data-left="${leftId}"]`, "right", board);
    const end = getConnectorPoint(`[data-right="${rightId}"]`, "left", board);
    const c1 = start.x + Math.max(42, (end.x - start.x) * 0.38);
    const c2 = end.x - Math.max(42, (end.x - start.x) * 0.38);
    lines.push(`<path class="done-line" d="M ${start.x} ${start.y} C ${c1} ${start.y}, ${c2} ${end.y}, ${end.x} ${end.y}" />`);
  });

  if (state.drag) {
    lines.push(`<line class="drag-line" x1="${state.drag.x1}" y1="${state.drag.y1}" x2="${state.drag.x2}" y2="${state.drag.y2}" />`);
  }

  svg.innerHTML = lines.join("");
}

